    private void processResponse()
    {
        // Clear out the response text area.
        txtaResponse.setText("");
        
        
        // Check for a valid question number range.
        
        if (questionNumber >= 1 && questionNumber <= 4)
        {
            processDogQuestion();
        }
        else // Invalid Question Number
        {
            txtaResponse.append("Application Error: Please contact the developer!\n");
        }
    }
            
    private void processDogQuestion()
    {
            // Do they like dogs?
        if (questionNumber >= 1)
        {
            if (dog == true) // Yes, they do.
            {
                   lblpicture.setIcon((Icon) (new ImageIcon(this.getClass().getResource("/images/dscf5037_thumb.jpg"))));
                   txtaResponse.append("You got fuzzed!\n");
            
                   lblQuestions.setText("Are reptiles cool?");
                   
                   processReptilesQuestion();
            }
            else // Well, they must be a cat instead.
            {
                   lblpicture.setIcon((Icon) (new ImageIcon(this.getClass().getResource("/images/dsc_0010_thumb.jpg"))));
                   txtaResponse.append("You are a Cat! Meow!\n");
                   
                               lblQuestions.setEnabled(false);
                               btnYes.setEnabled(false);
                               btnNo.setEnabled(false);
            }
       }
    }
                   
    private void processReptilesQuestion()
    {
        if (questionNumber >= 2)
        {
            if (reptiles == true)
            {
                   lblpicture.setIcon((Icon) (new ImageIcon(this.getClass().getResource("/images/alligator6_thumb.jpg"))));
                   txtaResponse.append("You must be a gator's fan!\n");
            
                   lblQuestions.setText("Do you like lizards?"); 
                   
                   processLizardQuestion();
            }
            else
            {
                
            }
                 if (reptiles == false)
                 {
                   lblpicture.setIcon((Icon) (new ImageIcon(this.getClass().getResource("/images/alligator6_thumb.jpg"))));
                   txtaResponse.append("Scared of those scaley things? That's alright.\n");
            
                   lblQuestions.setText("Are fish interesting to you?"); 
                   
                   processFishQuestion();
                 }
                 else
                 {
                     
                 }
        }
        else
        {
            
        }
    }
    
