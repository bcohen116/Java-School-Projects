private void btnYesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnYesMouseClicked

    switch (questionNumber)
    {
        case 0:
            dog = true;
            questionNumber++;
            break;
        case 1:
            reptiles = true;
            questionNumber++;
            break;
        case 2:
            lizards = true;
            fish = true;
            questionNumber++;
            break;
        case 3:
            mammals = true;
            questionNumber++;
            lblQuestions.setEnabled(false);
            btnYes.setEnabled(false);
            btnNo.setEnabled(false);
            break;
        default:
            break;
    }
    
    processResponse();
}//GEN-LAST:event_btnYesMouseClicked

