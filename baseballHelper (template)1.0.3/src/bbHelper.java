private void btnStartMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnStartMouseClicked

    // Enable controls associated with your line of questioning.
    lblQuestion.setEnabled(true);
    btnYes.setEnabled(true);
    btnNo.setEnabled(true);
    
   // Now begin the line of questioning...
    lblQuestion.setText("Do you like America's pastime?");
    txtaResponse.setText("");
    lblTeamPic.setIcon(null);
    questionNumber = 0;
    baseball = false;
    nationalLeague = false;
    redUniforms = false;
    ohio = false;
}//GEN-LAST:event_btnStartMouseClicked

