    private void processResponse()
    {
        // Clear out the response text area.
        txtaResponse.setText("");
        
        
        // Check for a valid question number range.
        
        if (questionNumber >= 1 && questionNumber <= 4)
        {
            processBaseballQuestion();
        }
        else // Invalid Question Number
        {
            txtaResponse.append("Application Error: Please contact the developer!\n");
        }
    }
    
    private void processBaseballQuestion()
    {
        // Does the user like baseball?
            
        if (questionNumber >= 1)
        {
            if (baseball) // Yes, they do.
            {
               lblTeamPic.setIcon((Icon) (new ImageIcon(this.getClass().getResource("/images/mlb2Fit.gif"))));
               txtaResponse.append("Play Ball!\n");

               processLeagueQuestion();
            }
            else // Well, maybe they like cricket.
            {
               lblTeamPic.setIcon((Icon) (new ImageIcon(this.getClass().getResource("/images/cricketFit.gif"))));
               txtaResponse.append("Been to India lately?\n");
            }
        }
    }
    
    private void processLeagueQuestion()
    {
        lblQuestion.setText("Do you prefer the National League?");

       if (questionNumber >= 2)
       {
           if (nationalLeague)
           {
               lblTeamPic.setIcon((Icon) (new ImageIcon(this.getClass().getResource("/images/NLFit.gif"))));
               txtaResponse.append("Welcome to the Majors!\n");

               processRedUniformsQuestion();
           }
           else
           {
               lblTeamPic.setIcon((Icon) (new ImageIcon(this.getClass().getResource("/images/oriolesFit.gif"))));
               txtaResponse.append("Must be an Orioles fan.\n");
           }
       }
    }
    private void processRedUniformsQuestion()
    {
                lblQuestion.setText("Do you like red uniforms?");

       if (questionNumber >= 3)
       {
           if (redUniforms)
           {
               lblTeamPic.setIcon((Icon) (new ImageIcon(this.getClass().getResource("/images/nlcFit.gif"))));
               txtaResponse.append("The Central Division is the best!\n");

               processOhioQuestion();
           }
           else
           {
               lblTeamPic.setIcon((Icon) (new ImageIcon(this.getClass().getResource("/images/dodgersFit.gif"))));
               txtaResponse.append("Good luck with the Dodgers.\n");
           }
       }
    }
    private void processOhioQuestion()
    {
                lblQuestion.setText("Are you also a Buckeye fan?");

       if (questionNumber >= 4)
       {
           if (ohio)
           {
               lblTeamPic.setIcon((Icon) (new ImageIcon(this.getClass().getResource("/images/redsFit.gif"))));
               txtaResponse.append("Go Reds!\n");
           }
           else
           {
               lblTeamPic.setIcon((Icon) (new ImageIcon(this.getClass().getResource("/images/cardinalsFit.gif"))));
               txtaResponse.append("Go Cards!\n");
           }
       }
    }
    
