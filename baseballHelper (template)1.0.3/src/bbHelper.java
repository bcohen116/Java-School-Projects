private void btnYesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnYesMouseClicked

    switch (questionNumber)
    {
        case 0:
            baseball = true;
            questionNumber++;
            break;
        case 1:
            nationalLeague = true;
            questionNumber++;
            break;
        case 2:
            redUniforms = true;
            questionNumber++;
            break;
        case 3:
            ohio = true;
            questionNumber++;
            lblQuestion.setEnabled(false);
            btnYes.setEnabled(false);
            btnNo.setEnabled(false);
            break;
        default:
            break;
    }
    
    processResponse();
}//GEN-LAST:event_btnYesMouseClicked

private void btnNoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnNoMouseClicked

    questionNumber++;
    processResponse();
    
    // For my flowchart, a 'no' response is the end.
    lblQuestion.setEnabled(false);
    btnYes.setEnabled(false);
    btnNo.setEnabled(false);
}//GEN-LAST:event_btnNoMouseClicked

